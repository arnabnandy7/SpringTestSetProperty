package com.model.Bean;

public class Emp 
{
	private int Empno;
	private String Empnm;
	private double Sal;
	
	public int getEmpno() {
		return Empno;
	}
	public void setEmpno(int empno) {
		Empno = empno;
	}
	public String getEmpnm() {
		return Empnm;
	}
	public void setEmpnm(String empnm) {
		Empnm = empnm;
	}
	public double getSal() {
		return Sal;
	}
	public void setSal(double sal) {
		Sal = sal;
	}
	

}
